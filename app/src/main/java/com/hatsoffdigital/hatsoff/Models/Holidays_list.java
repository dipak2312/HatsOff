package com.hatsoffdigital.hatsoff.Models;

public class Holidays_list {

    private String holiday_date;

    private String holiday_name;

    private String holiday_id;


    public String getHoliday_date() {
        return holiday_date;
    }

    public void setHoliday_date(String holiday_date) {
        this.holiday_date = holiday_date;
    }

    public String getHoliday_name() {
        return holiday_name;
    }

    public void setHoliday_name(String holiday_name) {
        this.holiday_name = holiday_name;
    }

    public String getHoliday_id() {
        return holiday_id;
    }

    public void setHoliday_id(String holiday_id) {
        this.holiday_id = holiday_id;
    }
}
